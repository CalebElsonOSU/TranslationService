# Usage: Write text to file "io.txt" in the folder where this file is running
# Start message with language code from the dictionary at the bottom of the file
# If main() finds a matching code, it translates the message and writes to file
# Sleeps for 5 seconds and checks again

from time import sleep
from pathlib import Path
from translatepy.translators.google import GoogleTranslate as Translator
io_address = "./io.txt"
translator = Translator()


def translate_input(input_text, language):
    return translator.translate(input_text, language).result


def write_translation(translated):
    with open(io_address, 'w') as io_file:
        io_file.write(translated)


def get_input():
    with open(io_address, "r") as io_file:
        return io_file.read()


def main():
    Path(io_address).touch(exist_ok=True)

    while True:
        io_input = get_input()

        try:
            io_input = str(io_input)
            language = io_input.split()[0]

            # If input language found, split from input string
            # Else raise exception
            if language in LANGUAGES:
                io_input = io_input.split(" ", 1)[1]
            else:
                raise IndexError

            write_translation(translate_input(io_input, language))
        except IndexError:
            pass

        sleep(5)


